    /**
     * Constrcutor for the Country Region validator
     *
     * Accepts a string country OR field country.
     *
     * @param array|Traversable $options
     */
    public function __construct($options = array())
    {
        if ($options instanceof Traversable) {
            $options = ArrayUtils::iteratorToArray($options);
        }

        if (array_key_exists('country', $options)) {
            $this->setCountry($options['country']);
        }

        parent::__construct($options);
    }

    /**
     * Sets the country for validation
     *
     * @param  string        $country
     * @return CountryRegion
     */
    public function setCountry($country)
    {
        $this->country = (string) $country;

        return $this;
    }

    /**
     * Get the country OR field
     *
     * @return string
     */
    public function getCountry()
    {
        return $this->country;
    }

    /**
     * Get Regions
     *
     * @param  string $country
     * @return array
     */
    public function getRegions($country = null)
    {
        if (is_null($country)) {
            return $this->regions;
        }

        return $this->regions[$country];
    }

    /**
     * Set Regions
     *
     * @param  array         $regions country => array(region_key => region_value)
     * @return CountryRegion
     */
    public function setRegions(array $regions)
    {
        $this->regions = $regions;
    }

    /**
     * Is Valid
     *
     * @param  mixed $value
     * @param  array $context
     * @return bool
     */
    public function isValid($value = null, $context = null)
    {
        $country = $this->getCountry();
        if (!isset($this->regions[$country])) {
            if (!isset($context[$country])) {
                $this->error(self::UNSUPPORTED);

                return false;
            }
            $country = $context[$country];
        }

        if (!is_string($value)) {
            $this->error(self::INVALID);

            return false;
        }

        $this->setValue($value);
        if (!array_key_exists($value, $this->regions[$country])) {
            $this->error(self::NO_MATCH);

            return false;
        }

        return true;
    }
